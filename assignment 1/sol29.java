public class sol29 {
    public static void main(String[] args) {
        //double instace
        double du =56.734d;
        Double d1=new Double(du);

        //double to byte
        byte d2=d1.byteValue();
        System.out.println("Double to byte conversion = "+d2);
        System.out.println();

         //double to short
         short d3=d1.shortValue();
         System.out.println("Double to short conversion = "+d3);
         System.out.println();


          //double to integer
        int d4=d1.intValue();
        System.out.println("Double to integer conversion = "+d4);
        System.out.println();

         //double to long
         long d5=d1.longValue();
         System.out.println("Double to long conversion = "+d5);
         System.out.println();

          //double to float
        float d6=d1.floatValue();
        System.out.println("Double to float conversion = "+d6);
        System.out.println();

         //double to double
         double d7=d1.doubleValue();
         System.out.println("Double to double conversion = "+d7);
         System.out.println();


    }
    
}
